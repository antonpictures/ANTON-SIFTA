    cfg = OctopusArmConfig(n_segments=16, n_arms=8)
    brain = OctopusBrain(cfg)

    # Phase 1: Set a goal and let arms reach
    goal = np.array([0.8, 0.8], dtype=np.float32)
    brain.set_goal(goal)

    initial_distance = float(np.linalg.norm(brain.body_center - goal))
    print(f"\n[*] Phase 1: Goal at {goal}, initial distance = {initial_distance:.3f}")

    for _ in range(20):
        state = brain.step()

    final_distance = float(np.linalg.norm(brain.body_center - goal))
    print(f"    After 20 steps: distance = {final_distance:.3f}, coherence = {state['coherence']:.3f}")

    # Phase 2: Sever an arm — it must still function
    print("\n[*] Phase 2: Severing arm 3 (Sumbre 2001)")
    severed = brain.sever_arm(3)
    assert severed.severed, "[FAIL] Arm was not marked as severed"

    # Continue stepping — the severed arm should still move
    pre_sever_tip = severed.tip.copy()
    severed_field = np.random.default_rng(67).uniform(0, 0.5, size=cfg.n_segments).astype(np.float32)
    severed.sense_environment(severed_field)
    severed.execute_motor_program(None)  # no goal — fully autonomous
    post_sever_tip = severed.tip
    severed_moved = float(np.linalg.norm(post_sever_tip - pre_sever_tip))
    print(f"    Severed arm displacement: {severed_moved:.4f}")
    assert severed_moved > 0.0, "[FAIL] Severed arm did not move autonomously"

    # Phase 3: Multi-arm coordination without CPG
    print("\n[*] Phase 3: Coordination without Central Pattern Generator (Levy 2015)")
    brain2 = OctopusBrain(cfg)
