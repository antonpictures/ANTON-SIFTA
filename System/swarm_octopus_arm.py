    coherences = []
    for _ in range(15):
        s = brain2.step()
        coherences.append(s["coherence"])
    mean_coherence = float(np.mean(coherences))
    print(f"    Mean coherence over 15 steps: {mean_coherence:.3f}")

    # Phase 4: Nonsomatotopic goal broadcast (Zullo 2009)
    print("\n[*] Phase 4: Nonsomatotopic goal broadcast (Zullo 2009)")
    brain3 = OctopusBrain(cfg)
