@dataclass
class OctopusArmConfig:
    """Configuration for one octopus arm (axial nerve cord)."""
    n_segments: int = 32          # segments along the arm (discrete axial nerve cord)
    n_arms: int = 8               # number of semi-autonomous arms
    wave_speed: float = 0.15      # propagation speed of stiffening wave (Gutfreund 2006)
    chemoreceptor_radius: float = 0.08  # sucker sensing radius in field coords
    elongation_force: float = 0.04      # push-by-elongation thrust (Levy 2015)
    goal_decay: float = 0.92      # how fast the brain's goal signal fades along the arm
    local_memory_deposit: float = 0.3   # arms deposit local memory (sucker chemoreception)
    autonomy: float = 0.7         # fraction of control that is local vs brain-directed
    eps: float = 1e-8


class OctopusArm:
    """
    A single semi-autonomous arm with its own peripheral motor program.

    Biology (Sumbre 2001): The arm contains a complete motor program
    for extension movements. Even severed from the brain, it can
    execute reaching. The 'axial nerve cord' runs the length of the
    arm with ~40M neurons per arm.
    """

    def __init__(self, arm_id: int, cfg: OctopusArmConfig):
        self.arm_id = arm_id
        self.cfg = cfg
        n = cfg.n_segments

        # Arm state: position of each segment in 2D field space
        angle = 2.0 * np.pi * arm_id / cfg.n_arms
        self.base = np.array([0.5, 0.5], dtype=np.float32)  # attached to body center
        self.segments = np.zeros((n, 2), dtype=np.float32)
        for i in range(n):
            t = (i + 1) / n
            self.segments[i] = self.base + t * 0.2 * np.array(
                [np.cos(angle), np.sin(angle)], dtype=np.float32
            )

        # Local 1D axial field (the arm's own peripheral nervous system)
        self.axial_field = np.zeros(n, dtype=np.float32)

        # Stiffening wave state (Gutfreund 2006)
        self.wave_front: float = 0.0  # position of the propagating wave [0, 1]
        self.wave_active: bool = False

        # Sucker chemoreception buffer (what this arm has tasted)
        self.chemoreception: List[float] = []

        # Severed flag — arm continues to function autonomously
        self.severed: bool = False

    @property
    def tip(self) -> np.ndarray:
        """Position of the distal tip (the reaching end)."""
        return self.segments[-1].copy()

    def inject_goal(self, goal_pos: np.ndarray) -> None:
        """
        Brain → Arm: inject a goal gradient along the axial nerve cord.
        Biology (Zullo 2009): The brain does NOT send specific motor
        commands. It sends a diffuse activation that propagates distally.
        """
        if self.severed:
            return  # severed arms ignore brain signals

        goal = np.asarray(goal_pos, dtype=np.float32)[:2]
        for i, seg in enumerate(self.segments):
            # Goal signal decays exponentially along the arm (proximal → distal)
            distance_to_goal = float(np.linalg.norm(seg - goal))
            decay = self.cfg.goal_decay ** i
            self.axial_field[i] += decay / (distance_to_goal + self.cfg.eps)

    def sense_environment(self, field_values: np.ndarray) -> float:
        """
        Sucker chemoreception: each segment's suckers taste the
        local environment (Graziadei 1971, Wells 1963).
        Returns the mean chemoreceptive signal across all suckers.

        The deposit is proportional to the local signal strength,
        creating a GRADIENT along the axial nerve cord that the
        peripheral motor program can follow autonomously.
        """
        total = 0.0
        for i in range(len(self.segments)):
            local = float(field_values[i]) if i < len(field_values) else 0.0
            # Deposit scales with signal — stronger taste = larger deposit
            # This creates the axial gradient the motor program needs
            self.axial_field[i] += local * self.cfg.local_memory_deposit * (1.0 + local)
            total += local
        mean_signal = total / max(len(self.segments), 1)
        self.chemoreception.append(mean_signal)
        return mean_signal

    def execute_motor_program(self, goal_pos: Optional[np.ndarray] = None) -> np.ndarray:
        """
        The peripheral motor program (Sumbre 2001).
        Each arm segment moves based on:
          1. Local axial field gradient (autonomous)
          2. Goal direction from brain (if not severed)
          3. Push-by-elongation mechanics (Levy 2015)

        Returns the tip displacement vector.
        """
        n = self.cfg.n_segments
        displacements = np.zeros((n, 2), dtype=np.float32)

        for i in range(n):
            # 1. Local gradient along the axial nerve cord
            if i > 0 and i < n - 1:
                local_grad = self.axial_field[i + 1] - self.axial_field[i - 1]
            elif i == 0:
                local_grad = self.axial_field[1] - self.axial_field[0]
            else:
                local_grad = self.axial_field[-1] - self.axial_field[-2]

            # Direction: toward the strongest axial signal (chemotaxis)
            # If connected to brain + goal, blend with radial direction
            radial = self.segments[i] - self.base
            radial_norm = float(np.linalg.norm(radial))
            if radial_norm > self.cfg.eps:
                radial_unit = radial / radial_norm
            else:
                radial_unit = np.array([1.0, 0.0], dtype=np.float32)

            # 2. Push-by-elongation: extend along the gradient direction
            # Use signed gradient — arms move toward higher concentrations
            elongation = self.cfg.elongation_force * local_grad
            local_displacement = radial_unit * elongation

            # 3. Brain-directed component (if connected)
            if goal_pos is not None and not self.severed:
                goal = np.asarray(goal_pos, dtype=np.float32)[:2]
                to_goal = goal - self.segments[i]
                goal_dist = float(np.linalg.norm(to_goal))
                if goal_dist > self.cfg.eps:
                    brain_pull = (1.0 - self.cfg.autonomy) * to_goal / goal_dist
                    local_pull = self.cfg.autonomy * local_displacement
                    displacements[i] = local_pull + brain_pull * self.cfg.elongation_force
                else:
                    displacements[i] = local_displacement
            else:
                # Fully autonomous (severed or no goal)
                displacements[i] = local_displacement

        # Apply displacements
        self.segments += displacements
        self.segments = np.clip(self.segments, 0.0, 1.0)

        # Decay axial field (pheromone evaporation along the nerve cord)
        self.axial_field *= self.cfg.goal_decay

        return displacements[-1]  # tip displacement


class OctopusBrain:
    """
    The central brain of the octopus.

    Biology (Zullo 2009): The higher motor centers are NOT somatotopically
    organized. Stimulating a brain region does not move a specific arm —
    it activates complex multi-arm coordinated movements.

    The brain's job:
      1. Select a goal (e.g., "food is at position X")
      2. Broadcast the goal to ALL arms simultaneously
      3. Let the arms figure out the motor execution locally
      4. Coordinate via propagating waves, not central timing
    """

    def __init__(self, cfg: Optional[OctopusArmConfig] = None):
        self.cfg = cfg or OctopusArmConfig()
        self.arms: List[OctopusArm] = [
            OctopusArm(i, self.cfg) for i in range(self.cfg.n_arms)
        ]
        self.goal: Optional[np.ndarray] = None
        self.body_center = np.array([0.5, 0.5], dtype=np.float32)

    def set_goal(self, goal_pos: np.ndarray) -> None:
        """Brain decides on a goal — broadcast to all arms."""
        self.goal = np.asarray(goal_pos, dtype=np.float32)[:2]
        for arm in self.arms:
            arm.inject_goal(self.goal)

    def sever_arm(self, arm_id: int) -> OctopusArm:
        """
        Sever an arm. It continues to function autonomously.
        Biology (Sumbre 2001): Severed arms execute reaching movements
        for up to an hour after separation.
        """
        if 0 <= arm_id < len(self.arms):
            self.arms[arm_id].severed = True
        return self.arms[arm_id]

    def step(self, environmental_field: Optional[np.ndarray] = None) -> Dict[str, object]:
        """
        One timestep of the octopus nervous system.

        1. Brain broadcasts goal to all arms
        2. Each arm senses its local environment (chemoreception)
        3. Each arm executes its peripheral motor program
        4. Coordination emerges from shared substrate
        """
        tip_positions = np.zeros((self.cfg.n_arms, 2), dtype=np.float32)
        tip_displacements = np.zeros((self.cfg.n_arms, 2), dtype=np.float32)
        chemoreceptive_signals = np.zeros(self.cfg.n_arms, dtype=np.float32)

        for i, arm in enumerate(self.arms):
            # Sense environment via suckers
            if environmental_field is not None:
                # Sample the environmental field at each arm segment
                field_samples = np.zeros(self.cfg.n_segments, dtype=np.float32)
                for j, seg in enumerate(arm.segments):
                    # Map segment position to field index
                    g = environmental_field.shape[0]
                    ix = int(np.clip(seg[0] * (g - 1), 0, g - 1))
                    iy = int(np.clip(seg[1] * (g - 1), 0, g - 1))
                    field_samples[j] = float(environmental_field[ix, iy])
                chemoreceptive_signals[i] = arm.sense_environment(field_samples)

            # Execute peripheral motor program
            disp = arm.execute_motor_program(self.goal)
            tip_positions[i] = arm.tip
            tip_displacements[i] = disp

        # Compute emergent coordination metrics
        # Levy (2015): coordination is measured by the coherence of
        # arm movements, not by central synchronization
        mean_tip = tip_positions.mean(axis=0)
        coherence = 1.0 - float(np.std(np.linalg.norm(
            tip_displacements, axis=1
        )))

        # Update body center (the mantle moves as the average of arm forces)
        total_force = tip_displacements.sum(axis=0)
        self.body_center += 0.1 * total_force
        self.body_center = np.clip(self.body_center, 0.1, 0.9)

        # Reattach arm bases to the (moved) body center
        for arm in self.arms:
            if not arm.severed:
                arm.base = self.body_center.copy()

        return {
            "tip_positions": tip_positions,
            "body_center": self.body_center.copy(),
            "coherence": float(np.clip(coherence, 0.0, 1.0)),
            "chemoreception_mean": float(chemoreceptive_signals.mean()),
            "severed_count": sum(1 for a in self.arms if a.severed),
        }

    def deposit_to_field(self, field: np.ndarray) -> np.ndarray:
        """
        Arms deposit chemoreceptive memory back into the shared field.
        This is the octopus's contribution to the stigmergic substrate.
        """
        for arm in self.arms:
            for seg in arm.segments:
                g = field.shape[0]
                ix = int(np.clip(seg[0] * (g - 1), 0, g - 1))
                iy = int(np.clip(seg[1] * (g - 1), 0, g - 1))
                field[ix, iy] += self.cfg.local_memory_deposit * 0.1
        return field


