_TURING_RAMP: str = " .·:+*#@"


def render_pattern_ascii(
    sim: GrayScottSimulator, *, width: int | None = None
) -> str:
    """Render the activator field as an ASCII heatmap."""
    v = sim.v
    if width is not None and width != v.shape[1]:
        # Subsample columns to fit.
        step = max(1, v.shape[1] // width)
        v = v[:, ::step]
    # Subsample rows too — terminal rows are ~2× wider than tall char.
    v = v[::2, :]
    vmax = float(v.max())
    if vmax <= 0:
        vmax = 1.0
    rows = []
    rows.append(
        f"Gray-Scott RD pattern (F={sim.config.feed}, k={sim.config.kill}, "
        f"steps={sim.step_count}, variance={sim.field_variance():.4f})"
    )
    for r in v:
        line = []
        for val in r:
            frac = max(0.0, min(1.0, float(val) / vmax))
            line.append(_TURING_RAMP[int(frac * (len(_TURING_RAMP) - 1))])
        rows.append("  " + "".join(line))
    return "\n".join(rows)


