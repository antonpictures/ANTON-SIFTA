@dataclass
class GrayScottConfig:
    """Parameters of the Gray-Scott RD system.

        ∂u/∂t = D_u ∇²u − u v² + F (1 − u)
        ∂v/∂t = D_v ∇²v + u v² − (F + k) v

    Pearson 1993 (Science 261, 189) gives the canonical (F, k) atlas:
    - (0.030, 0.062)  spots
    - (0.025, 0.055)  spots + stripes
    - (0.040, 0.060)  labyrinths / stripes
    - (0.018, 0.051)  mitosis (replicating spots)
    """

    shape: tuple[int, int] = (96, 96)
    D_u: float = 0.16
    D_v: float = 0.08
    feed: float = 0.035            # F
    kill: float = 0.065            # k
    dx: float = 1.0
    dt: float = 1.0
    truth_label: str = TRUTH_LABEL

    def __post_init__(self) -> None:
        if len(self.shape) != 2:
            raise ValueError("Gray-Scott grid must be 2D")
        if any(s < 4 for s in self.shape):
            raise ValueError("each grid axis must be ≥ 4")
        if self.D_u <= 0 or self.D_v <= 0:
            raise ValueError("diffusion constants must be positive")
        if not (self.D_u > self.D_v):
            raise ValueError(
                "Turing instability requires D_u > D_v (inhibitor "
                "diffuses faster than activator); Murray 1989 §2"
            )


def _laplacian_2d_periodic(arr: np.ndarray, dx: float = 1.0) -> np.ndarray:
    """2D Laplacian via 5-point stencil with periodic boundaries."""
    return (
        np.roll(arr, +1, axis=0) + np.roll(arr, -1, axis=0)
        + np.roll(arr, +1, axis=1) + np.roll(arr, -1, axis=1)
        - 4.0 * arr
    ) / (dx * dx)


class GrayScottSimulator:
    """Pure-numpy Gray-Scott reaction-diffusion simulator on a 2D grid.

    The activator `v` and substrate `u` evolve under the Gray-Scott
    equations with periodic boundaries. Seed the system with a small
    central perturbation; under (F, k) inside the Pearson 1993 atlas,
    visible patterns emerge.
    """

    def __init__(
        self,
        config: GrayScottConfig,
        *,
        seed: int | None = None,
    ) -> None:
        self.config = config
        self.rng = np.random.default_rng(seed)
        h, w = config.shape
        # Initial state: u = 1 everywhere, v = 0 everywhere
        self.u: np.ndarray = np.ones(config.shape, dtype=np.float64)
        self.v: np.ndarray = np.zeros(config.shape, dtype=np.float64)
        # Central perturbation: a small square where (u, v) flips to
        # (0.5, 0.25) + tiny noise.
        cy, cx = h // 2, w // 2
        r = max(2, min(h, w) // 10)
        self.u[cy - r:cy + r, cx - r:cx + r] = 0.5
        self.v[cy - r:cy + r, cx - r:cx + r] = 0.25
        self.u += self.rng.normal(0.0, 1e-3, size=config.shape)
        self.v += self.rng.normal(0.0, 1e-3, size=config.shape)
        self.step_count = 0

    def step(self) -> None:
        cfg = self.config
        lap_u = _laplacian_2d_periodic(self.u, dx=cfg.dx)
        lap_v = _laplacian_2d_periodic(self.v, dx=cfg.dx)
        uvv = self.u * self.v * self.v
        du = cfg.D_u * lap_u - uvv + cfg.feed * (1.0 - self.u)
        dv = cfg.D_v * lap_v + uvv - (cfg.feed + cfg.kill) * self.v
        self.u = self.u + cfg.dt * du
        self.v = self.v + cfg.dt * dv
        self.step_count += 1

    def run(self, steps: int) -> None:
        for _ in range(int(steps)):
            self.step()

    # ── Observables ─────────────────────────────────────────────────────
    def field_variance(self) -> float:
        """Variance of the activator field v. High variance ⇒ pattern."""
        return float(np.var(self.v))

    def is_patterned(self, threshold: float = 0.003) -> bool:
        """Did the system develop a non-uniform pattern?

        Threshold default 0.003 is empirical for the Gray-Scott
        spot/stripe regime — well above the ~1e-6 floor of random
        fluctuation but well below the ~0.02 of a saturated pattern.
        """
        return self.field_variance() > threshold

    def peak_count(self, threshold: float = 0.4) -> int:
        """Rough count of activator peaks (4-connectivity flood fill).

        A peak is a connected region where `v > threshold`. Useful as a
        coarse pattern classifier (many small peaks = spots; few large
        connected regions = labyrinth / stripes).
        """
        mask = self.v > threshold
        if not mask.any():
            return 0
        # Manual 4-connected labelling (avoid scipy.ndimage).
        labels = np.zeros(mask.shape, dtype=np.int32)
        next_label = 0
        h, w = mask.shape
        for i in range(h):
            for j in range(w):
                if mask[i, j] and labels[i, j] == 0:
                    next_label += 1
                    # BFS
                    stack = [(i, j)]
                    while stack:
                        y, x = stack.pop()
                        if labels[y, x] != 0 or not mask[y, x]:
                            continue
                        labels[y, x] = next_label
                        if y + 1 < h:
                            stack.append((y + 1, x))
                        if y - 1 >= 0:
                            stack.append((y - 1, x))
                        if x + 1 < w:
                            stack.append((x + 1, x + 1) if False else (y, x + 1))
                        if x - 1 >= 0:
                            stack.append((y, x - 1))
        return int(next_label)

    def snapshot(self) -> dict[str, Any]:
        return {
            "schema": TRUTH_LABEL,
            "ts": time.time(),
            "shape": list(self.config.shape),
            "D_u": self.config.D_u,
            "D_v": self.config.D_v,
            "feed": self.config.feed,
            "kill": self.config.kill,
            "step_count": self.step_count,
            "v_min": float(self.v.min()),
            "v_max": float(self.v.max()),
            "v_mean": float(self.v.mean()),
            "v_variance": self.field_variance(),
            "is_patterned": self.is_patterned(),
            "truth_guard": TURING_TRUTH_GUARD,
        }


