# ── AST dry-run validator ─────────────────────────────────────────────────────

def _ast_validate(script: str, organ_id: str) -> tuple[bool, str]:
    """Parse *script* with the Python AST.  Returns (ok, detail)."""
    try:
        _ast.parse(script)
        return True, "AST parse OK"
    except SyntaxError as exc:
        return False, f"SyntaxError in {organ_id} script: {exc}"


@dataclass(frozen=True)
class LabProtocol:
    spec_organ_id: str
    hardware_target: str
    protocol_steps: tuple[str, ...]
    script_content: str
    safety_gate_enforced: bool
    truth_label: str = "HYPOTHESIS"
    ast_valid: bool = True
    ast_detail: str = "AST parse OK"

    @property
    def is_valid(self) -> bool:
        return self.safety_gate_enforced and len(self.protocol_steps) > 0 and self.ast_valid

    @property
    def proof_of_property(self) -> dict[str, Any]:
        return {
            "E48": "Physical Protocol Generation",
            "organ_id": self.spec_organ_id,
            "hardware_target": self.hardware_target,
            "steps": len(self.protocol_steps),
            "safety_gate_enforced": self.safety_gate_enforced,
            "ast_valid": self.ast_valid,
            "falsifier": "Protocol execution bypasses E34 safety gate OR script fails AST parse = BROKEN.",
            "truth_label": self.truth_label,
        }

