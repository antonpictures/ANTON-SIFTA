def generate_all_protocols() -> list[LabProtocol]:
    """Generate LabProtocol for every spec in the wet-dry bridge."""
    bridge = wet_dry_bridge()
    return [generate_protocol_for_spec(spec) for spec in bridge.specs]


# ── ProtocolLibrary — index for organ-router queries ─────────────────────────

@dataclass
class ProtocolLibrary:
    """Index of all generated LabProtocols, queryable by organ ID.

    Used by the organ query router and the Stigmerobotics widget to give
    Alice deterministic answers about what physical protocols exist.
    """
    protocols: list[LabProtocol] = field(default_factory=list)

    def __post_init__(self) -> None:
        if not self.protocols:
            self.protocols = generate_all_protocols()

    def by_organ(self, organ_id: str) -> LabProtocol | None:
        """Return the protocol for *organ_id*, or None if not found."""
        organ_id = organ_id.upper()
        return next((p for p in self.protocols if p.spec_organ_id == organ_id), None)

    def all_valid(self) -> bool:
        """True if every protocol passes safety gate + AST validation."""
        return all(p.is_valid for p in self.protocols)

    def summary(self) -> str:
        lines = [f"E48 ProtocolLibrary — {len(self.protocols)} protocols:"]
        for p in self.protocols:
            status = "✅" if p.is_valid else "❌"
            lines.append(f"  {status} [{p.spec_organ_id}] {p.hardware_target} ({len(p.protocol_steps)} steps, AST:{p.ast_detail})")
        lines.append(f"All valid: {self.all_valid()}")
        return "\n".join(lines)

    def proof_of_property(self) -> dict[str, Any]:
        return {
            "E48": "ProtocolLibrary",
            "total_protocols": len(self.protocols),
            "all_valid": self.all_valid(),
            "coverage": [p.spec_organ_id for p in self.protocols],
            "all_ast_valid": all(p.ast_valid for p in self.protocols),
            "falsifier": "any p.is_valid==False or p.ast_valid==False = E48 BROKEN",
            "truth_label": "HYPOTHESIS",
        }


